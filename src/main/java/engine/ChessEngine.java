	public static final List<Move> shuffleMovesList(List<Move> ls) {
		List<Move> shuffled = new ArrayList<Move>(ls.size());
		for (int index : SHUFFLE_TABLE[ls.size()]) {
			shuffled.add(ls.get(index));
		}
		return shuffled;
	}

	public static final List<Move> removeMovesRandomly(List<Move> list, double perc) {
		perc = perc < 0.0 ? 0.0 : (perc > 1.0 ? 1.0 : perc);
		int count = (int) (perc * list.size());
		int randIndex;
		Random randObj = new Random();
		for (int i = 0; i < count; i++) {
			randIndex = randObj.nextInt(count - i);
			list.remove(randIndex);
		}
		return list;
	}

